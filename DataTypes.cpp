#include <iostream>
#include <string>
using namespace std;

int main()
{

    // Declare variables of different data types
    int integerVar = 42;                // Integer type. Whole number type
    float floatVar = 3.14;              // Floating-point type.
    double doubleVar = 2.718281828459;  // Double precision floating-point type
    char charVar = 'A';                 // Character type
    bool boolVar = true;                // Boolean type
    string stringVar = "Hello, World!"; // String type

    // Output the values of the variables
    cout << "Integer: " << integerVar << endl;
    cout << "Float: " << floatVar << endl;
    cout << "Double: " << doubleVar << endl;
    cout << "Character: " << charVar << endl;
    cout << "Boolean: " << boolVar << endl;
    cout << "String: " << stringVar << endl;

    return 0;
}